print('''Задача №3, Сколько яблок было у Анны, сколько съел Пол.
Программа считает сколько яблок осталось''')

a = int(input('Введите сколько яблок было у Анны: '))
p = int(input('Введите сколько яблок съел Пол: '))
b = a - p
print('Всего у Анны было', a, 'яблок(а).', 'Пол съел', p, 'яблок(а).', 'Осталось', b, 'яблок(а,о).')
