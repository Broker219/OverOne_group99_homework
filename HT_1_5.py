# Улитка ползет по дереву высота которого 20м. В день она проползает расстояние в 2м, но за ночь сползает на 1м.
# Программа находит день, на который улитка доберется до вершины.
# условно: 12 часов день, 12 часов ночь

d = 0
while d < 20:
    d = d + 2 - 1
    print(d)
print('День на который улитка доберется до вершины равен - ', d)
