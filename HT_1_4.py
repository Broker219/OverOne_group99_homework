# Два поезда, расстояние между которыми 200 км, едут по прямой на встречу друг другу со скоростью 50 км/ч.
# Между ними от одного поезда к дурому летает муха со скоростью 75 км/ч. Программа находит какое расстояние
# пролетит муха до встречи поездов.

t = int((200 / 50) / 2) # время за которое поезда встрется
print('Через', t, 'часа встретятся поезда.')

r = int(t * 75)
print('Муха пролетит', r, 'километров до встречи поездов.')
